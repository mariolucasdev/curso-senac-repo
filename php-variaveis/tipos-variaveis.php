<?php
// Tipos de Variáveis

// String - Carrega um valor de texto (string)
$nome = "Mário Lucas <br>";
echo $nome;

// Inteiro - Carrega um valor inteiro (int)
$idade = 27 . "<br>";
echo $idade;

// Boleano - Carregao um valor boleano (bool)
$aprovado = true;
echo $aprovado . "<br>";

// Float - Carrega um valor de ponto flutuante (float)
$altura = 1.6;
echo $altura;
?>